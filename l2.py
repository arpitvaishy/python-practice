n = int(input())
for i in range(1, n + 1):
    print(i, end=" ")

    '''Key Points:
input():

Reads input from the user as a string.
int(input()) converts the input to an integer.
range(1, n + 1):

Generates numbers starting from 1 up to n (inclusive).
print(i, end=""):

Prints the current number i without moving to a new line.
The end="" argument ensures that the numbers are printed on the same line with spaces.'''